InputGrade = int(input("กรอกคะแนน : "))
if InputGrade >= 80:
    print("A")
elif InputGrade >= 75:
    print("B+")
elif InputGrade >= 70:
    print("B")
elif InputGrade >= 65:
    print("C+")
elif InputGrade >= 60:
    print("C")
elif InputGrade >= 55:
    print("D+")
elif InputGrade >= 50:
    print("D")
else:
    print("F")